#include<iostream>
using namespace std;
int main(){
    for(int i = 1;i<=10;i=i+1)
    {
        cout<<"hello\n";
    }
    // counting print
    for(int i = 0;i<=10;i=i+2)
    {
        cout<<i<<endl;
    }
    // countFrom 10 to 1
    for(int i = 10;i>=0;i =i-1)
    {
        cout<<i<<endl;
    }
}